function setupSlider(slider, auto = true) {
  if (!slider) return;

  const slides = Array.from(slider.querySelectorAll(".hero-slide"));
  const prevBtn = slider.querySelector(".hero-slider-btn.prev");
  const nextBtn = slider.querySelector(".hero-slider-btn.next");
  const dotsContainer = slider.querySelector(".hero-dots");

  if (!slides.length || !dotsContainer) return;

  let current = 0;
  let timer = null;

  slides.forEach((_, index) => {
    const dot = document.createElement("button");
    if (index === 0) dot.classList.add("active");
    dot.addEventListener("click", () => goToSlide(index, true));
    dotsContainer.appendChild(dot);
  });

  const dots = Array.from(dotsContainer.querySelectorAll("button"));

  function setActive(index) {
    slides.forEach((s, i) => {
      s.classList.toggle("active", i === index);
    });
    dots.forEach((d, i) => {
      d.classList.toggle("active", i === index);
    });
  }

  function goToSlide(index, userAction = false) {
    current = (index + slides.length) % slides.length;
    setActive(current);
    if (userAction && auto) {
      restartAuto();
    }
  }

  function next() {
    goToSlide(current + 1);
  }

  function prev() {
    goToSlide(current - 1);
  }

  function startAuto() {
    if (!auto) return;
    timer = setInterval(next, 3000);
  }

  function restartAuto() {
    if (timer) clearInterval(timer);
    startAuto();
  }

  if (nextBtn) nextBtn.addEventListener("click", () => next() || (auto && restartAuto()));
  if (prevBtn) prevBtn.addEventListener("click", () => prev() || (auto && restartAuto()));

  startAuto();
}

function initHeroSliders() {
  const mainSlider = document.getElementById("mosque-slider");
  const fullSlider = document.getElementById("mosque-slider-full");
  const heroSlider = document.getElementById("mosque-hero-slider");
  setupSlider(mainSlider, true);
  setupSlider(fullSlider, false);
  setupSlider(heroSlider, true);
}

