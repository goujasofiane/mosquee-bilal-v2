let currentLang = "fr";

function applyTranslations(lang) {
  currentLang = lang;
  const dict = translations[lang];
  if (!dict) return;

  document
    .querySelectorAll("[data-i18n]")
    .forEach((el) => {
      const key = el.getAttribute("data-i18n");
      if (dict[key]) {
        el.textContent = dict[key].replace(
          "{year}",
          new Date().getFullYear().toString()
        );
      }
    });

  document
    .querySelectorAll("[data-i18n-placeholder]")
    .forEach((el) => {
      const key = el.getAttribute("data-i18n-placeholder");
      if (dict[key]) {
        el.setAttribute("placeholder", dict[key]);
      }
    });

  document.documentElement.lang = lang === "ar" ? "ar" : "fr";
  document.documentElement.dir = lang === "ar" ? "rtl" : "ltr";

  document
    .querySelectorAll(".lang-switch button")
    .forEach((btn) => btn.classList.toggle("active", btn.dataset.lang === lang));

  try {
    document.dispatchEvent(new CustomEvent("translationsApplied"));
  } catch (e) {}
}

function initLanguageSwitch() {
  document
    .querySelectorAll(".lang-switch button")
    .forEach((btn) => {
      btn.addEventListener("click", () => {
        applyTranslations(btn.dataset.lang);
      });
    });
}

function initNavToggle() {
  const toggle = document.querySelector(".nav-toggle");
  const navList = document.querySelector(".main-nav ul");
  if (!toggle || !navList) return;

  toggle.addEventListener("click", () => {
    navList.classList.toggle("open");
  });

  navList.addEventListener("click", (e) => {
    if (e.target.tagName === "A") {
      navList.classList.remove("open");
    }
  });
}

function setupSlider(slider, auto = true) {
  if (!slider) return;

  const slides = Array.from(slider.querySelectorAll(".hero-slide"));
  const prevBtn = slider.querySelector(".hero-slider-btn.prev");
  const nextBtn = slider.querySelector(".hero-slider-btn.next");
  const dotsContainer = slider.querySelector(".hero-dots");

  if (!slides.length || !dotsContainer) return;

  let current = 0;
  let timer = null;

  slides.forEach((_, index) => {
    const dot = document.createElement("button");
    if (index === 0) dot.classList.add("active");
    dot.addEventListener("click", () => goToSlide(index, true));
    dotsContainer.appendChild(dot);
  });

  const dots = Array.from(dotsContainer.querySelectorAll("button"));

  function setActive(index) {
    slides.forEach((s, i) => {
      s.classList.toggle("active", i === index);
    });
    dots.forEach((d, i) => {
      d.classList.toggle("active", i === index);
    });
  }

  function goToSlide(index, userAction = false) {
    current = (index + slides.length) % slides.length;
    setActive(current);
    if (userAction && auto) {
      restartAuto();
    }
  }

  function next() {
    goToSlide(current + 1);
  }

  function prev() {
    goToSlide(current - 1);
  }

  function startAuto() {
    if (!auto) return;
    timer = setInterval(next, 5000);
  }

  function restartAuto() {
    if (timer) clearInterval(timer);
    startAuto();
  }

  if (nextBtn) nextBtn.addEventListener("click", () => next() || (auto && restartAuto()));
  if (prevBtn) prevBtn.addEventListener("click", () => prev() || (auto && restartAuto()));

  startAuto();
}

function initHeroSliders() {
  const mainSlider = document.getElementById("mosque-slider");
  const fullSlider = document.getElementById("mosque-slider-full");
  const heroSlider = document.getElementById("mosque-hero-slider");
  setupSlider(mainSlider, true);
  setupSlider(fullSlider, false);
  setupSlider(heroSlider, true);
}

function initScrollReveal() {
  const elements = document.querySelectorAll(
    ".section, .home-gallery-card, .contact-card, .social-card, .donation-text-card, .mosquee-masonry-item"
  );
  if (!elements.length) return;

  elements.forEach((el) => el.classList.add("reveal"));

  const observer = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          entry.target.classList.add("is-visible");
          observer.unobserve(entry.target);
        }
      });
    },
    { threshold: 0.15 }
  );

  elements.forEach((el) => observer.observe(el));
}

function initGalleryLightbox() {
  const thumbs = document.querySelectorAll(
    ".home-gallery-grid img, .mosquee-masonry-grid img"
  );
  const lightbox = document.getElementById("gallery-lightbox");
  if (!thumbs.length || !lightbox) return;

  const lightboxImg = lightbox.querySelector("img");
  const closeBtn = lightbox.querySelector(".gallery-lightbox-close");
  const prevBtn = lightbox.querySelector(".gallery-lightbox-prev");
  const nextBtn = lightbox.querySelector(".gallery-lightbox-next");
  const sources = Array.from(thumbs).map((img) => ({
    src: img.src,
    alt: img.alt || "",
  }));
  let currentIndex = 0;

  function showAt(index) {
    currentIndex = (index + sources.length) % sources.length;
    lightboxImg.src = sources[currentIndex].src;
    lightboxImg.alt = sources[currentIndex].alt;
  }

  function openLightbox(index) {
    showAt(index);
    lightbox.classList.add("open");
    lightbox.setAttribute("aria-hidden", "false");
    document.body.style.overflow = "hidden";
  }

  function closeLightbox() {
    lightbox.classList.remove("open");
    lightbox.setAttribute("aria-hidden", "true");
    document.body.style.overflow = "";
  }

  thumbs.forEach((img, index) => {
    img.addEventListener("click", () => {
      openLightbox(index);
    });
  });

  if (closeBtn) {
    closeBtn.addEventListener("click", closeLightbox);
  }

  if (prevBtn) {
    prevBtn.addEventListener("click", () => showAt(currentIndex - 1));
  }

  if (nextBtn) {
    nextBtn.addEventListener("click", () => showAt(currentIndex + 1));
  }

  lightbox.addEventListener("click", (e) => {
    if (e.target.classList.contains("gallery-lightbox-backdrop")) {
      closeLightbox();
    }
  });

  document.addEventListener("keydown", (e) => {
    if (e.key === "Escape" && lightbox.classList.contains("open")) {
      closeLightbox();
    } else if (e.key === "ArrowLeft" && lightbox.classList.contains("open")) {
      showAt(currentIndex - 1);
    } else if (e.key === "ArrowRight" && lightbox.classList.contains("open")) {
      showAt(currentIndex + 1);
    }
  });
}

function initDonationPopup() {
  const btn = document.getElementById("donation-popup-btn");
  if (!btn) return;
  btn.addEventListener("click", (e) => {
    e.preventDefault();
    window.open(
      "https://www.cotizup.com/achever-mosquee-bilal",
      "cotizup",
      "width=600,height=700,scrollbars=yes"
    );
  });
}

function initYear() {
  const yearSpan = document.getElementById("year");
  if (yearSpan) {
    yearSpan.textContent = new Date().getFullYear().toString();
  }
}

document.addEventListener("DOMContentLoaded", () => {
  initLanguageSwitch();
  initNavToggle();
  initHeroSliders();
  initScrollReveal();
  initGalleryLightbox();
  initDonationPopup();
  initYear();
  applyTranslations("fr");
});

