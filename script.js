let currentLang = "fr";

function applyTranslations(lang) {
  currentLang = lang;
  const dict = translations[lang];
  if (!dict) return;

  document
    .querySelectorAll("[data-i18n]")
    .forEach((el) => {
      const key = el.getAttribute("data-i18n");
      if (!dict[key]) return;

      const translated = dict[key].replace(
        "{year}",
        new Date().getFullYear().toString()
      );

      // Si la traduction contient du markup HTML (ex: <br />), on doit le conserver via innerHTML.
      // Sinon, on utilise textContent pour éviter d'interpréter du HTML.
      if (/<[^>]+>/.test(translated)) {
        el.innerHTML = translated;
      } else {
        el.textContent = translated;
      }
    });

  document
    .querySelectorAll("[data-i18n-placeholder]")
    .forEach((el) => {
      const key = el.getAttribute("data-i18n-placeholder");
      if (dict[key]) {
        el.setAttribute("placeholder", dict[key]);
      }
    });

  document.documentElement.lang = lang === "ar" ? "ar" : "fr";

  // Exigences RTL: forcer placement switch FR/AR à droite et logo à gauche.
  const langSwitch = document.querySelector(".lang-switch");
  if (langSwitch) langSwitch.classList.add("lang-switcher");

  const logoBrand = document.querySelector(".nh-brand");
  if (logoBrand) logoBrand.classList.add("logo-brand");

  document
    .querySelectorAll(".lang-switch button")
    .forEach((btn) => btn.classList.toggle("active", btn.dataset.lang === lang));

  try {
    document.dispatchEvent(new CustomEvent("translationsApplied"));
  } catch (e) {}
}

function initLanguageSwitch() {
  document
    .querySelectorAll(".lang-switch button")
    .forEach((btn) => {
      btn.addEventListener("click", () => {
        const lang = btn.dataset.lang;
        const isAr = lang === "ar";

        // Exigence: mise à jour explicite lang/dir côté document.
        document.documentElement.lang = isAr ? "ar" : "fr";

        // Exigence: persistance sous la clé 'lang'.
        try {
          localStorage.setItem("lang", isAr ? "ar" : "fr");
        } catch (e) {}

        applyTranslations(lang);
      });
    });
}

function initNavToggle() {
  const toggle = document.querySelector(".nav-toggle");
  const navList = document.querySelector(".main-nav ul");
  if (!toggle || !navList) return;

  toggle.addEventListener("click", () => {
    navList.classList.toggle("open");
  });

  navList.addEventListener("click", (e) => {
    if (e.target.tagName === "A") {
      navList.classList.remove("open");
    }
  });
}

