let currentLang = "fr";

function applyTranslations(lang) {
  currentLang = lang;
  const dict = translations[lang];
  if (!dict) return;

  document
    .querySelectorAll("[data-i18n]")
    .forEach((el) => {
      const key = el.getAttribute("data-i18n");
      if (dict[key]) {
        el.textContent = dict[key].replace(
          "{year}",
          new Date().getFullYear().toString()
        );
      }
    });

  document
    .querySelectorAll("[data-i18n-placeholder]")
    .forEach((el) => {
      const key = el.getAttribute("data-i18n-placeholder");
      if (dict[key]) {
        el.setAttribute("placeholder", dict[key]);
      }
    });

  document.documentElement.lang = lang === "ar" ? "ar" : "fr";
  document.documentElement.dir = lang === "ar" ? "rtl" : "ltr";

  document
    .querySelectorAll(".lang-switch button")
    .forEach((btn) => btn.classList.toggle("active", btn.dataset.lang === lang));
}

function initLanguageSwitch() {
  document
    .querySelectorAll(".lang-switch button")
    .forEach((btn) => {
      btn.addEventListener("click", () => {
        applyTranslations(btn.dataset.lang);
      });
    });
}

function initNavToggle() {
  const toggle = document.querySelector(".nav-toggle");
  const navList = document.querySelector(".main-nav ul");
  if (!toggle || !navList) return;

  toggle.addEventListener("click", () => {
    navList.classList.toggle("open");
  });

  navList.addEventListener("click", (e) => {
    if (e.target.tagName === "A") {
      navList.classList.remove("open");
    }
  });
}

function setupSlider(slider, auto = true) {
  if (!slider) return;

  const slides = Array.from(slider.querySelectorAll(".hero-slide"));
  const prevBtn = slider.querySelector(".hero-slider-btn.prev");
  const nextBtn = slider.querySelector(".hero-slider-btn.next");
  const dotsContainer = slider.querySelector(".hero-dots");

  if (!slides.length || !dotsContainer) return;

  let current = 0;
  let timer = null;

  slides.forEach((_, index) => {
    const dot = document.createElement("button");
    if (index === 0) dot.classList.add("active");
    dot.addEventListener("click", () => goToSlide(index, true));
    dotsContainer.appendChild(dot);
  });

  const dots = Array.from(dotsContainer.querySelectorAll("button"));

  function setActive(index) {
    slides.forEach((s, i) => {
      s.classList.toggle("active", i === index);
    });
    dots.forEach((d, i) => {
      d.classList.toggle("active", i === index);
    });
  }

  function goToSlide(index, userAction = false) {
    current = (index + slides.length) % slides.length;
    setActive(current);
    if (userAction && auto) {
      restartAuto();
    }
  }

  function next() {
    goToSlide(current + 1);
  }

  function prev() {
    goToSlide(current - 1);
  }

  function startAuto() {
    if (!auto) return;
    timer = setInterval(next, 5000);
  }

  function restartAuto() {
    if (timer) clearInterval(timer);
    startAuto();
  }

  if (nextBtn) nextBtn.addEventListener("click", () => next() || (auto && restartAuto()));
  if (prevBtn) prevBtn.addEventListener("click", () => prev() || (auto && restartAuto()));

  startAuto();
}

function initHeroSliders() {
  const mainSlider = document.getElementById("mosque-slider");
  const fullSlider = document.getElementById("mosque-slider-full");
  setupSlider(mainSlider, true);
  setupSlider(fullSlider, false);
}

function initDonationPopup() {
  const btn = document.getElementById("donation-popup-btn");
  if (!btn) return;
  btn.addEventListener("click", (e) => {
    e.preventDefault();
    window.open(
      "https://www.cotizup.com/achever-mosquee-bilal",
      "cotizup",
      "width=600,height=700,scrollbars=yes"
    );
  });
}

function initYear() {
  const yearSpan = document.getElementById("year");
  if (yearSpan) {
    yearSpan.textContent = new Date().getFullYear().toString();
  }
}

document.addEventListener("DOMContentLoaded", () => {
  initLanguageSwitch();
  initNavToggle();
  initHeroSliders();
  initDonationPopup();
  initYear();
  applyTranslations("fr");
});

