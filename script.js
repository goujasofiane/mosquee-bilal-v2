let currentLang = "fr";
let conversations = [];
let currentConversationId = null;

function applyTranslations(lang) {
  currentLang = lang;
  const dict = translations[lang];
  if (!dict) return;

  document
    .querySelectorAll("[data-i18n]")
    .forEach((el) => {
      const key = el.getAttribute("data-i18n");
      if (dict[key]) {
        el.textContent = dict[key].replace(
          "{year}",
          new Date().getFullYear().toString()
        );
      }
    });

  document
    .querySelectorAll("[data-i18n-placeholder]")
    .forEach((el) => {
      const key = el.getAttribute("data-i18n-placeholder");
      if (dict[key]) {
        el.setAttribute("placeholder", dict[key]);
      }
    });

  document.documentElement.lang = lang === "ar" ? "ar" : "fr";
  document.documentElement.dir = lang === "ar" ? "rtl" : "ltr";

  document
    .querySelectorAll(".lang-switch button")
    .forEach((btn) => btn.classList.toggle("active", btn.dataset.lang === lang));
}

function initLanguageSwitch() {
  document
    .querySelectorAll(".lang-switch button")
    .forEach((btn) => {
      btn.addEventListener("click", () => {
        applyTranslations(btn.dataset.lang);
      });
    });
}

function initNavToggle() {
  const toggle = document.querySelector(".nav-toggle");
  const navList = document.querySelector(".main-nav ul");
  if (!toggle || !navList) return;

  toggle.addEventListener("click", () => {
    navList.classList.toggle("open");
  });

  navList.addEventListener("click", (e) => {
    if (e.target.tagName === "A") {
      navList.classList.remove("open");
    }
  });
}

function setupSlider(slider, auto = true) {
  if (!slider) return;

  const slides = Array.from(slider.querySelectorAll(".hero-slide"));
  const prevBtn = slider.querySelector(".hero-slider-btn.prev");
  const nextBtn = slider.querySelector(".hero-slider-btn.next");
  const dotsContainer = slider.querySelector(".hero-dots");

  if (!slides.length || !dotsContainer) return;

  let current = 0;
  let timer = null;

  slides.forEach((_, index) => {
    const dot = document.createElement("button");
    if (index === 0) dot.classList.add("active");
    dot.addEventListener("click", () => goToSlide(index, true));
    dotsContainer.appendChild(dot);
  });

  const dots = Array.from(dotsContainer.querySelectorAll("button"));

  function setActive(index) {
    slides.forEach((s, i) => {
      s.classList.toggle("active", i === index);
    });
    dots.forEach((d, i) => {
      d.classList.toggle("active", i === index);
    });
  }

  function goToSlide(index, userAction = false) {
    current = (index + slides.length) % slides.length;
    setActive(current);
    if (userAction && auto) {
      restartAuto();
    }
  }

  function next() {
    goToSlide(current + 1);
  }

  function prev() {
    goToSlide(current - 1);
  }

  function startAuto() {
    if (!auto) return;
    timer = setInterval(next, 5000);
  }

  function restartAuto() {
    if (timer) clearInterval(timer);
    startAuto();
  }

  if (nextBtn) nextBtn.addEventListener("click", () => next() || (auto && restartAuto()));
  if (prevBtn) prevBtn.addEventListener("click", () => prev() || (auto && restartAuto()));

  startAuto();
}

function initHeroSliders() {
  const mainSlider = document.getElementById("mosque-slider");
  const fullSlider = document.getElementById("mosque-slider-full");
  setupSlider(mainSlider, true);
  setupSlider(fullSlider, false);
}

function addAssistantMessage(content) {
  const messages = document.getElementById("ai-messages");
  if (!messages) return;
  const bubble = document.createElement("div");
  bubble.className = "ai-message assistant";
  bubble.innerHTML = content;
  messages.appendChild(bubble);
  messages.scrollTop = messages.scrollHeight;

  appendMessageToConversation("assistant", content);
}

function addUserMessage(text) {
  const messages = document.getElementById("ai-messages");
  if (!messages) return;
  const bubble = document.createElement("div");
  bubble.className = "ai-message user";
  bubble.textContent = text;
  messages.appendChild(bubble);
  messages.scrollTop = messages.scrollHeight;

  appendMessageToConversation("user", text);
}

async function callIslamicAI(question, lang) {
  try {
    const response = await fetch("/api/chat", {
      method: "POST",
      headers: {
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        message: question,
        language: lang === "ar" ? "ar" : "fr",
      }),
    });

    if (!response.ok) {
      throw new Error("API error");
    }

    const data = await response.json();
    const text =
      typeof data.reply === "string" && data.reply.trim().length
        ? data.reply.trim()
        : lang === "ar"
        ? "تعذّر الحصول على إجابة من الخادم الآن."
        : "Impossible d’obtenir une réponse du serveur pour le moment.";

    addAssistantMessage(text.replace(/\n/g, "<br>"));
  } catch (e) {
    const err =
      lang === "ar"
        ? "حدث خطأ في الاتصال بالخادم. يُرجى المحاولة لاحقًا."
        : "Une erreur est survenue lors de la connexion au serveur. Veuillez réessayer plus tard.";
    addAssistantMessage(`<em>${err}</em>`);
  }
}

function initAIChat() {
  const form = document.getElementById("ai-form");
  const input = document.getElementById("ai-input");
  if (!form || !input) return;

  form.addEventListener("submit", async (e) => {
    e.preventDefault();
    const question = input.value.trim();
    if (!question) return;
    addUserMessage(question);
    input.value = "";
    showTypingIndicator();
    await callIslamicAI(question, currentLang);
    hideTypingIndicator();
  });
}

function initAIWelcome() {
  const messages = document.getElementById("ai-messages");
  if (!messages || messages.childElementCount > 0) return;

  const textFr =
    "As-salâm ‘alaykoum,\n\nTu peux me poser des questions sur la croyance (‘aqîda), le fiqh, la vie quotidienne ou l’éthique islamique. J’essaierai de répondre en m’appuyant sur le Coran et la Sunna authentique, en citant les références quand c’est possible.";
  const textAr =
    "السلام عليكم ورحمة الله وبركاته,\n\nيمكنك أن تطرح أسئلتك عن العقيدة أو الفقه أو الحياة اليومية أو الأخلاق الإسلامية، وسأحاول أن أجيب اعتمادًا على القرآن الكريم والسنة الصحيحة مع ذكر المراجع قدر الإمكان.";

  addAssistantMessage(currentLang === "ar" ? textAr.replace(/\n/g, "<br>") : textFr.replace(/\n/g, "<br>"));
}

function showTypingIndicator() {
  const messages = document.getElementById("ai-messages");
  if (!messages) return;
  const div = document.createElement("div");
  div.className = "ai-message assistant ai-typing";
  div.innerHTML = '<span class="dot"></span><span class="dot"></span><span class="dot"></span>';
  messages.appendChild(div);
  messages.scrollTop = messages.scrollHeight;
}

function hideTypingIndicator() {
  document.querySelectorAll(".ai-message.ai-typing").forEach((el) => el.remove());
}

function loadConversations() {
  try {
    const raw = localStorage.getItem("bilal_conversations");
    if (!raw) return [];
    const parsed = JSON.parse(raw);
    if (Array.isArray(parsed)) return parsed;
  } catch {
    // ignore
  }
  return [];
}

function saveConversations() {
  try {
    localStorage.setItem("bilal_conversations", JSON.stringify(conversations));
  } catch {
    // ignore
  }
}

function createConversation() {
  const id = Date.now().toString();
  const conv = {
    id,
    title: currentLang === "ar" ? "محادثة جديدة" : "Nouvelle conversation",
    messages: [],
  };
  conversations.unshift(conv);
  currentConversationId = id;
  saveConversations();
  renderConversationList();
  renderConversationMessages();
}

function appendMessageToConversation(role, content) {
  if (!currentConversationId) {
    createConversation();
  }
  const conv = conversations.find((c) => c.id === currentConversationId);
  if (!conv) return;
  conv.messages.push({ role, content });
  if (role === "user" && conv.messages.length === 1) {
    conv.title = content.slice(0, 40);
  }
  saveConversations();
  renderConversationList();
}

function renderConversationList() {
  const list = document.getElementById("conversations-list");
  if (!list) return;
  list.innerHTML = "";
  conversations.forEach((conv) => {
    const li = document.createElement("li");
    li.textContent = conv.title || (currentLang === "ar" ? "محادثة" : "Conversation");
    li.className = conv.id === currentConversationId ? "conversation-item active" : "conversation-item";
    li.addEventListener("click", () => {
      currentConversationId = conv.id;
      renderConversationList();
      renderConversationMessages();
    });
    list.appendChild(li);
  });
}

function renderConversationMessages() {
  const messagesEl = document.getElementById("ai-messages");
  if (!messagesEl) return;
  messagesEl.innerHTML = "";
  const conv = conversations.find((c) => c.id === currentConversationId);
  if (!conv || !conv.messages.length) {
    initAIWelcome();
    return;
  }
  conv.messages.forEach((m) => {
    if (m.role === "user") {
      addUserMessage(m.content);
    } else {
      addAssistantMessage(m.content);
    }
  });
}

function initConversations() {
  const list = document.getElementById("conversations-list");
  const newBtn = document.getElementById("new-chat-btn");
  if (!list || !newBtn) return;

  conversations = loadConversations();
  if (!conversations.length) {
    createConversation();
  } else {
    currentConversationId = conversations[0].id;
  }

  renderConversationList();
  renderConversationMessages();

  newBtn.addEventListener("click", () => {
    createConversation();
  });
}

function applyTheme(theme) {
  const t = theme === "dark" ? "dark" : "light";
  document.body.classList.toggle("dark-theme", t === "dark");
  try {
    localStorage.setItem("bilal_theme", t);
  } catch {
    // ignore
  }
  const btn = document.getElementById("theme-toggle");
  if (btn) btn.textContent = t === "dark" ? "☀️" : "🌙";
}

function initTheme() {
  let stored = "light";
  try {
    stored = localStorage.getItem("bilal_theme") || "light";
  } catch {
    stored = "light";
  }
  applyTheme(stored);
  const btn = document.getElementById("theme-toggle");
  if (btn) {
    btn.addEventListener("click", () => {
      const next = document.body.classList.contains("dark-theme") ? "light" : "dark";
      applyTheme(next);
    });
  }
}

function initSidebarToggle() {
  const sidebar = document.querySelector(".chat-sidebar");
  const toggle = document.getElementById("sidebar-toggle");
  if (!sidebar || !toggle) return;
  toggle.addEventListener("click", () => {
    sidebar.classList.toggle("open");
  });
}

function initYear() {
  const yearSpan = document.getElementById("year");
  if (yearSpan) {
    yearSpan.textContent = new Date().getFullYear().toString();
  }
}

document.addEventListener("DOMContentLoaded", () => {
  initLanguageSwitch();
  initNavToggle();
  initHeroSliders();
  initAIChat();
  initAIWelcome();
  initConversations();
  initTheme();
  initSidebarToggle();
  initYear();
  applyTranslations("fr");
});

